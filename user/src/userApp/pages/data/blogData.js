/**
 * HELPER FUNCTIONS
 */

export const allBlogs = Object.values(blogData);

export const getBlogBySlug = (slug) => blogData[slug];

export const getBlogsByCategory = (category) =>
  allBlogs.filter((blog) => blog.category === category);

export const getBlogsByTag = (tag) =>
  allBlogs.filter((blog) => blog.tags?.includes(tag));

export const getRecentBlogs = (excludeSlug, limit = 4) =>
  allBlogs
    .filter((blog) => blog.slug !== excludeSlug)
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);

export const getFeaturedBlogs = () =>
  allBlogs.filter((blog) => blog.featured);

export const getAllCategories = () => [
  ...new Set(allBlogs.map((blog) => blog.category)),
];

export const getAllTags = () => [
  ...new Set(allBlogs.flatMap((blog) => blog.tags || [])),
];

export const searchBlogs = (query) => {
  const lowerQuery = query.toLowerCase();
  return allBlogs.filter(
    (blog) =>
      blog.title.toLowerCase().includes(lowerQuery) ||
      blog.excerpt.toLowerCase().includes(lowerQuery) ||
      blog.content.toLowerCase().includes(lowerQuery)
  );
};